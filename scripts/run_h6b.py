# Analyze one panel
# ---------------------------------------------------------------------------
def analyze_panel(panel, v8_path, utag_path):
    """Run H6b analysis for one panel. Returns dict of results."""
    print(f"\n{'='*70}")
    print(f"H6b: Co-occurrence Entropy — {panel}-panel")
    print(f"{'='*70}")

    # Load data
    print(f"\nLoading {panel}-panel v8 data...")
    f_v8 = h5py.File(v8_path, 'r')
    f_utag = h5py.File(utag_path, 'r')

    sample_ids = load_array(f_v8, 'sample_id')
    tma_arr = load_array(f_v8, 'tma')
    cell_types = load_array(f_v8, 'cell_type')
    tumor_mask = get_tumor_mask(sample_ids)
    cx = f_v8['obs']['centroid_x'][:]
    cy = f_v8['obs']['centroid_y'][:]

    n_total = len(sample_ids)
    n_tumor = np.sum(tumor_mask)
    print(f"  {n_total:,} total -> {n_tumor:,} tumor cells (controls excluded)")

    # Classify UTAG domains
    print(f"\nClassifying UTAG domains...")
    domains, foll_domains, inter_domains = classify_domains(f_utag, cell_types, panel)

    # --- Per-ROI entropy computation ---
    print(f"\nComputing per-ROI entropies (k={K_NEIGHBORS})...")
    unique_rois = sorted(set(sample_ids[tumor_mask]))

    roi_data = {}
    for i_roi, roi in enumerate(unique_rois):
        rm = (sample_ids == roi) & tumor_mask
        ct_roi = cell_types[rm]
        tma_val = tma_arr[rm][0]

        # Count typed cells
        typed = ct_roi[~np.isin(ct_roi, list(LQ_TYPES))]
        n_typed = len(typed)

        # Min cell threshold
        min_cells = MIN_CELLS_BIOMAX if tma_val == 'Biomax' else MIN_CELLS_INHOUSE
        if n_typed < min_cells:
            continue

        # Phenotypic entropy
        H_pheno = compute_phenotypic_entropy(ct_roi)

        # Interaction entropy
        cx_roi = cx[rm]
        cy_roi = cy[rm]
        H_interact, cooc_matrix = compute_interaction_entropy(cx_roi, cy_roi, ct_roi, k=K_NEIGHBORS)

        if np.isnan(H_pheno) or np.isnan(H_interact):
            continue

        # Domain-level interaction entropy
        dom_roi = domains[rm]
        foll_cells = np.isin(dom_roi, foll_domains)
        inter_cells = np.isin(dom_roi, inter_domains)

        # Follicular interaction entropy
        n_foll = np.sum(foll_cells & ~np.isin(ct_roi, list(LQ_TYPES)))
        if n_foll >= 100:
            H_inter_foll, _ = compute_interaction_entropy(
                cx_roi[foll_cells], cy_roi[foll_cells], ct_roi[foll_cells])
        else:
            H_inter_foll = np.nan

        # Interfollicular interaction entropy
        n_inter = np.sum(inter_cells & ~np.isin(ct_roi, list(LQ_TYPES)))
        if n_inter >= 100:
            H_inter_inter, _ = compute_interaction_entropy(
                cx_roi[inter_cells], cy_roi[inter_cells], ct_roi[inter_cells])
        else:
            H_inter_inter = np.nan

        roi_data[roi] = {
            'tma': tma_val,
            'n_typed': n_typed,
            'H_pheno': H_pheno,
            'H_interact': H_interact,
            'H_inter_foll': H_inter_foll,
            'H_inter_inter': H_inter_inter,
        }

        if (i_roi + 1) % 20 == 0:
            print(f"  Processed {i_roi+1}/{len(unique_rois)} ROIs...")

    print(f"\n  ROIs passing filters: {len(roi_data)}")

    # --- Summary statistics ---
    rois = sorted(roi_data.keys())
    H_phenos = np.array([roi_data[r]['H_pheno'] for r in rois])
    H_interacts = np.array([roi_data[r]['H_interact'] for r in rois])
    tma_vals = np.array([roi_data[r]['tma'] for r in rois])

    print(f"\nPhenotypic entropy:")
    print(f"  Mean: {np.mean(H_phenos):.3f}, Std: {np.std(H_phenos):.3f}, "
          f"Range: [{np.min(H_phenos):.3f}, {np.max(H_phenos):.3f}]")
    print(f"\nInteraction entropy:")
    print(f"  Mean: {np.mean(H_interacts):.3f}, Std: {np.std(H_interacts):.3f}, "
          f"Range: [{np.min(H_interacts):.3f}, {np.max(H_interacts):.3f}]")

    # --- Correlation: interaction vs phenotypic entropy ---
    rho_main, p_main = spearmanr(H_phenos, H_interacts)
    r_pearson, p_pearson = pearsonr(H_phenos, H_interacts)
    print(f"\nInteraction entropy vs Phenotypic entropy:")
    print(f"  Spearman rho={rho_main:.3f}, p={p_main:.2e}")
    print(f"  Pearson  r={r_pearson:.3f}, p={p_pearson:.2e}")

    # --- Residual analysis: partial out phenotypic entropy ---
    # Regress H_interact ~ H_pheno, take residuals
    z = np.polyfit(H_phenos, H_interacts, 1)
    H_interact_predicted = np.polyval(z, H_phenos)
    residuals = H_interacts - H_interact_predicted
    residual_std = np.std(residuals)
    residual_range = np.max(residuals) - np.min(residuals)
    print(f"\nResidual analysis (H_interact | H_pheno):")
    print(f"  Residual std: {residual_std:.4f}")
    print(f"  Residual range: {residual_range:.4f}")
    print(f"  Coefficient of determination R^2: {r_pearson**2:.3f}")
    print(f"  Unexplained variance: {1 - r_pearson**2:.3f}")

    # What drives the residuals? Cell type fractions that correlate with
    # interaction entropy AFTER controlling for phenotypic entropy
    roi_residual = {r: res for r, res in zip(rois, residuals)}

    # --- Domain-level interaction entropy ---
    H_inter_folls = np.array([roi_data[r]['H_inter_foll'] for r in rois])
    H_inter_inters = np.array([roi_data[r]['H_inter_inter'] for r in rois])

    valid_domain = ~np.isnan(H_inter_folls) & ~np.isnan(H_inter_inters)
    n_domain = np.sum(valid_domain)
    print(f"\nDomain-level interaction entropy ({n_domain} ROIs with both domains):")

    if n_domain >= 10:
        foll_vals = H_inter_folls[valid_domain]
        inter_vals = H_inter_inters[valid_domain]
        u_stat, p_domain = mannwhitneyu(foll_vals, inter_vals, alternative='two-sided')
        print(f"  Follicular: mean={np.mean(foll_vals):.3f}, std={np.std(foll_vals):.3f}")
        print(f"  Interfollicular: mean={np.mean(inter_vals):.3f}, std={np.std(inter_vals):.3f}")
        print(f"  Mann-Whitney U: p={p_domain:.2e}")
        diff_pct = (np.mean(inter_vals) - np.mean(foll_vals)) / np.mean(foll_vals) * 100
        print(f"  Interfollicular {'higher' if diff_pct > 0 else 'lower'} by {abs(diff_pct):.1f}%")
    else:
        p_domain = np.nan
        print("  Insufficient ROIs with both domains")

    # --- Cell type drivers of interaction entropy ---
    print(f"\nCell type drivers of interaction entropy:")
    roi_fracs = compute_roi_celltype_fractions(sample_ids, cell_types, tumor_mask, LQ_TYPES)
    roi_interact_metric = {r: roi_data[r]['H_interact'] for r in rois}
    drivers_interact = correlate_celltype_with_metric(roi_fracs, roi_interact_metric)

    for ct_name, rho, p_d in drivers_interact[:12]:
        print(f"  {ct_name:40s}  rho={rho:+.3f}  p={p_d:.2e}")

    # Drivers of RESIDUAL interaction entropy (spatial information beyond composition)
    print(f"\nDrivers of RESIDUAL interaction entropy (independent of phenotypic):")
    drivers_residual = correlate_celltype_with_metric(roi_fracs, roi_residual)
    for ct_name, rho, p_d in drivers_residual[:12]:
        print(f"  {ct_name:40s}  rho={rho:+.3f}  p={p_d:.2e}")

    # --- Per-TMA breakdown ---
    tmas = sorted(set(tma_vals))
    print(f"\nPer-TMA interaction entropy:")
    tma_groups = {}
    for t in tmas:
        mask_t = tma_vals == t
        vals_t = H_interacts[mask_t]
        tma_groups[t] = vals_t
        print(f"  {t:>8}: n={len(vals_t):>3}, mean={np.mean(vals_t):.3f}, "
              f"std={np.std(vals_t):.3f}")

    # Kruskal-Wallis across TMAs
    kw_groups = [tma_groups[t] for t in tmas if len(tma_groups[t]) > 0]
    if len(kw_groups) >= 2:
        H_kw, p_kw = kruskal(*kw_groups)
        print(f"  Kruskal-Wallis: H={H_kw:.2f}, p={p_kw:.4f}")
    else:
        p_kw = np.nan

    # --- Leave-one-TMA-out sensitivity ---
    print(f"\nLeave-one-TMA-out sensitivity:")
    loo_results = []
    for exclude in tmas:
        keep = tma_vals != exclude
        if np.sum(keep) < 10:
            loo_results.append((exclude, np.nan, np.nan))
            continue
        rho_loo, p_loo = spearmanr(H_phenos[keep], H_interacts[keep])
        # Also compute R^2 for residual analysis
        r_loo, _ = pearsonr(H_phenos[keep], H_interacts[keep])
        loo_results.append((exclude, rho_loo, 1 - r_loo**2))
        print(f"  Excl {exclude}: Spearman rho={rho_loo:.3f}, p={p_loo:.2e}, "
              f"unexplained var={1 - r_loo**2:.3f}")

    # Keep arrays for figure generation before closing files
    result = {
        'panel': panel,
        'rois': rois,
        'roi_data': roi_data,
        'H_phenos': H_phenos,
        'H_interacts': H_interacts,
        'tma_vals': tma_vals,
        'tmas': tmas,
        'tma_groups': tma_groups,
        'rho_main': rho_main,
        'p_main': p_main,
        'r_pearson': r_pearson,
        'residuals': residuals,
        'residual_std': residual_std,
        'roi_residual': roi_residual,
        'H_inter_folls': H_inter_folls,
        'H_inter_inters': H_inter_inters,
        'p_domain': p_domain,
        'drivers_interact': drivers_interact,
        'drivers_residual': drivers_residual,
        'loo_results': loo_results,
        'p_kw': p_kw,
        'sample_ids': sample_ids,
        'cell_types': cell_types,
        'tumor_mask': tumor_mask,
        'cx': cx,
        'cy': cy,
    }

    f_v8.close()
    f_utag.close()

    return result


