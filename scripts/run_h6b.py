"""H6b: Spatial interaction diversity (co-occurrence entropy).

Tests whether the diversity of cell-cell spatial interactions (which types
neighbor which) adds independent information beyond phenotypic entropy
(cell type proportions alone).

Method:
  - For each ROI, build kNN graph (k=10) on cell centroids (scipy KDTree)
  - Record each cell's type and its k neighbors' types -> co-occurrence matrix
  - Normalize to probabilities, compute Shannon entropy on flattened distribution
  - Compare "interaction entropy" with "phenotypic entropy" (H6a)
  - Test whether interaction entropy adds independent spatial information

Analysis:
  1. Compute both entropies per ROI for both panels
  2. Scatter: interaction entropy vs phenotypic entropy — Spearman correlation
  3. Domain-level: interaction entropy in follicular vs interfollicular domains
  4. Cell type drivers of interaction entropy
  5. Leave-one-TMA-out sensitivity
  6. Residual analysis: does interaction entropy predict anything beyond phenotypic?

Output:
  - output/hypotheses_v8/fig_h6b_Both.png (main figure)
  - output/hypotheses_v8/fig_h6b_Both_supp.png (supplementary)
"""

