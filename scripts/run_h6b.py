# Main figure (2x3) — combined Both panels
# ---------------------------------------------------------------------------
def make_main_figure(results_T, results_S):
    """Generate main H6b figure with combined results from both panels."""
    print("\n--- Generating main figure ---")
    fig, axes = plt.subplots(2, 3, figsize=(18, 11))

    # (a) Concept cartoon
    add_cartoon(axes[0, 0], CARTOON_PATH)
    label_panel(axes[0, 0], 'a')

    # (b) Scatter: interaction vs phenotypic entropy (both panels)
    ax = axes[0, 1]
    for res, marker, panel_label in [(results_T, 'o', 'T'), (results_S, 's', 'S')]:
        for t in res['tmas']:
            mask_t = res['tma_vals'] == t
            ax.scatter(res['H_phenos'][mask_t], res['H_interacts'][mask_t],
                       c=TMA_COLORS.get(t, 'gray'), alpha=0.5, s=20,
                       marker=marker, edgecolors='white', linewidths=0.3,
                       label=f'{t} ({panel_label})')
    # Regression lines
    for res, color, ls in [(results_T, '#2c3e50', '-'), (results_S, '#8e44ad', '--')]:
        z = np.polyfit(res['H_phenos'], res['H_interacts'], 1)
        x_line = np.linspace(np.min(res['H_phenos']), np.max(res['H_phenos']), 50)
        ax.plot(x_line, np.polyval(z, x_line), color=color, linestyle=ls, lw=1.5,
                alpha=0.7, label=f'{res["panel"]}: rho={res["rho_main"]:.3f}')

    ax.set_xlabel('Phenotypic Entropy (bits)', fontsize=10)
    ax.set_ylabel('Interaction Entropy (bits)', fontsize=10)
    p_T = results_T['p_main']
    p_S = results_S['p_main']
    ax.set_title(f'Interaction vs Phenotypic Entropy\n'
                 f'T: rho={results_T["rho_main"]:.3f}, p={p_T:.1e}\n'
                 f'S: rho={results_S["rho_main"]:.3f}, p={p_S:.1e}',
                 fontsize=9)
    ax.legend(fontsize=5.5, loc='upper left', ncol=2)
    label_panel(ax, 'b')

    # (c) Domain-level interaction entropy (follicular vs interfollicular)
    ax = axes[0, 2]
    box_data = []
    box_labels = []
    box_colors = []

    for res in [results_T, results_S]:
        panel = res['panel']
        valid = ~np.isnan(res['H_inter_folls']) & ~np.isnan(res['H_inter_inters'])
        if np.sum(valid) >= 5:
            foll_v = res['H_inter_folls'][valid]
            inter_v = res['H_inter_inters'][valid]
            box_data.extend([foll_v, inter_v])
            box_labels.extend([f'{panel}\nFollicular\n(n={len(foll_v)})',
                               f'{panel}\nInterfoll.\n(n={len(inter_v)})'])
            box_colors.extend(['#FFDDDD', '#DDDDFF'])

    if len(box_data) > 0:
        bp = ax.boxplot(box_data, tick_labels=box_labels, patch_artist=True, widths=0.5)
        for patch, c in zip(bp['boxes'], box_colors):
            patch.set_facecolor(c)
            patch.set_alpha(0.8)
        # Add jittered points
        for i, data in enumerate(box_data):
            jitter = np.random.RandomState(42 + i).normal(0, 0.04, len(data))
            ax.scatter(np.ones(len(data)) * (i + 1) + jitter, data,
                       c='gray', alpha=0.3, s=8, edgecolors='none', zorder=5)

        # Significance annotations
        bracket_y = max(np.max(d) for d in box_data) * 1.02
        for panel_idx, res in enumerate([results_T, results_S]):
            if not np.isnan(res['p_domain']):
                x1 = panel_idx * 2 + 1
                x2 = panel_idx * 2 + 2
                y_br = bracket_y + panel_idx * bracket_y * 0.08
                ax.plot([x1, x1, x2, x2], [y_br, y_br * 1.02, y_br * 1.02, y_br],
                        'k-', lw=0.8)
                p_str = f'p={res["p_domain"]:.1e}' if res['p_domain'] < 0.001 else f'p={res["p_domain"]:.3f}'
                ax.text((x1 + x2) / 2, y_br * 1.03, p_str, ha='center', fontsize=7)

    ax.set_ylabel('Interaction Entropy (bits)', fontsize=10)
    ax.set_title('Interaction Entropy by Domain', fontsize=10)
    ax.tick_params(axis='x', labelsize=7)
    label_panel(ax, 'c')

    # (d) Representative cores (T-panel, low vs high interaction entropy)
    ax = axes[1, 0]
    res = results_T
    roi_metric = {r: res['roi_data'][r]['H_interact'] for r in res['rois']}
    plot_representative_core_spatial(
        ax, res['cx'], res['cy'], res['sample_ids'], res['cell_types'],
        res['tumor_mask'], roi_metric,
        label_lo='Low H_interact', label_hi='High H_interact',
        metric_name='H_int', top_n_types=10,
        min_cells=MIN_CELLS_INHOUSE
    )
    ax.set_title('Representative Cores (T-panel)', fontsize=10)
    label_panel(ax, 'd')

    # (e) Residual analysis — histograms of residuals for both panels
    ax = axes[1, 1]
    for res, color, panel_label in [(results_T, '#2c3e50', 'T'), (results_S, '#8e44ad', 'S')]:
        ax.hist(res['residuals'], bins=25, alpha=0.5, color=color,
                edgecolor='white', label=f'{panel_label} (std={res["residual_std"]:.3f})')
    ax.axvline(0, color='gray', linestyle='--', lw=1)
    ax.set_xlabel('Residual Interaction Entropy (bits)', fontsize=10)
    ax.set_ylabel('Number of ROIs', fontsize=10)
    R2_T = results_T['r_pearson'] ** 2
    R2_S = results_S['r_pearson'] ** 2
    ax.set_title(f'Residual H_interact | H_pheno\n'
                 f'T: R$^2$={R2_T:.3f}, unexpl.={1-R2_T:.3f}\n'
                 f'S: R$^2$={R2_S:.3f}, unexpl.={1-R2_S:.3f}', fontsize=9)
    ax.legend(fontsize=8)
    label_panel(ax, 'e')

    # (f) Drivers of RESIDUAL interaction entropy (T-panel)
    ax = axes[1, 2]
