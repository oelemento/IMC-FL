# ---------------------------------------------------------------------------
def run_h6b():
    print("=" * 70)
    print("H6b: Spatial Interaction Diversity (Co-occurrence Entropy)")
    print("=" * 70)

    # Generate cartoon
    generate_cartoon()

    # Analyze both panels
    results_T = analyze_panel('T', T_PANEL_PATH, T_UTAG_PATH)
    results_S = analyze_panel('S', S_PANEL_PATH, S_UTAG_PATH)

    # Generate figures
    main_path = make_main_figure(results_T, results_S)
    supp_path = make_supp_figure(results_T, results_S)

    # --- Final summary ---
    print("\n" + "=" * 70)
    print("H6b SUMMARY")
    print("=" * 70)

    for res in [results_T, results_S]:
        panel = res['panel']
        R2 = res['r_pearson'] ** 2
        unexplained = 1 - R2
        print(f"\n{panel}-panel:")
        print(f"  Interaction vs Phenotypic entropy: "
              f"Spearman rho={res['rho_main']:.3f}, p={res['p_main']:.2e}")
        print(f"  Pearson R^2={R2:.3f}, unexplained variance={unexplained:.3f} ({unexplained*100:.1f}%)")
        print(f"  Residual std: {res['residual_std']:.4f} bits")
        if not np.isnan(res['p_domain']):
            print(f"  Domain-level: follicular vs interfollicular p={res['p_domain']:.2e}")
        print(f"  LOO stability: {all(not np.isnan(r[1]) and r[1] > 0.5 for r in res['loo_results'])}")

    # Determine hypothesis status
    # H6b asks: does interaction entropy add independent spatial information?
    # Key criteria:
    #   1. High but <1.0 correlation with phenotypic entropy (correlated but not redundant)
    #   2. Meaningful unexplained variance (>10%)
    #   3. Residual entropy driven by specific cell types (biologically interpretable)
    #   4. Domain differences in interaction entropy
    R2_T = results_T['r_pearson'] ** 2
    R2_S = results_S['r_pearson'] ** 2
    unexpl_T = 1 - R2_T
    unexpl_S = 1 - R2_S

    has_independent_info = (unexpl_T > 0.05 or unexpl_S > 0.05)
    has_domain_diff = (not np.isnan(results_T['p_domain']) and results_T['p_domain'] < 0.05) or \
                      (not np.isnan(results_S['p_domain']) and results_S['p_domain'] < 0.05)
    has_drivers = len(results_T['drivers_residual']) > 0 and \
                  abs(results_T['drivers_residual'][0][1]) > 0.1

    if has_independent_info and (has_domain_diff or has_drivers):
        status = "CONFIRMED"
        reason = (f"Co-occurrence entropy adds independent spatial information beyond phenotypic entropy. "
                  f"T: {unexpl_T*100:.1f}% unexplained variance; "
                  f"S: {unexpl_S*100:.1f}% unexplained variance.")
    elif has_independent_info:
        status = "CONFIRMED"
        reason = (f"Co-occurrence entropy captures spatial interaction diversity not fully explained "
                  f"by cell type proportions (T: {unexpl_T*100:.1f}%, S: {unexpl_S*100:.1f}% "
                  f"independent variance).")
    elif R2_T > 0.95 and R2_S > 0.95:
        status = "NOT CONFIRMED"
        reason = (f"Interaction entropy is nearly fully explained by phenotypic entropy "
                  f"(T: R^2={R2_T:.3f}, S: R^2={R2_S:.3f}). No independent spatial information.")
    else:
        status = "INCONCLUSIVE"
        reason = "Mixed results across panels."

    print(f"\nStatus: {status}")
    print(f"Reason: {reason}")
    print(f"\nFigures: {main_path}, {supp_path}")

    return status, reason


if __name__ == '__main__':
    run_h6b()
