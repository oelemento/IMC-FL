# ---------------------------------------------------------------------------
# Phenotypic entropy (Shannon on cell type proportions)
# ---------------------------------------------------------------------------
def compute_phenotypic_entropy(cell_types_roi):
    """Shannon entropy on cell type proportions, excluding LQ types."""
    clean = cell_types_roi[~np.isin(cell_types_roi, list(LQ_TYPES))]
    if len(clean) < 20:
        return np.nan
    counts = Counter(clean)
    total = sum(counts.values())
    props = np.array([c / total for c in counts.values()])
    props = props[props > 0]
    return -np.sum(props * np.log2(props))


# ---------------------------------------------------------------------------
# Interaction entropy (Shannon on kNN co-occurrence matrix)
# ---------------------------------------------------------------------------
def compute_interaction_entropy(cx_roi, cy_roi, cell_types_roi, k=K_NEIGHBORS):
    """Compute co-occurrence entropy from kNN graph on cell centroids.

    1. Build kNN graph (k neighbors) on cell coordinates
    2. For each cell, record (cell_type, neighbor_type) pairs
    3. Build co-occurrence matrix, normalize to probabilities
    4. Shannon entropy on flattened probability distribution

    Excludes LQ/Unassigned cells from both the graph and the counts.
    """
    # Filter to typed cells only
    typed_mask = ~np.isin(cell_types_roi, list(LQ_TYPES))
    if np.sum(typed_mask) < 50:
        return np.nan, None

    x = cx_roi[typed_mask]
    y = cy_roi[typed_mask]
    ct = cell_types_roi[typed_mask]

    # Get unique types in this ROI
    unique_types = sorted(set(ct))
    type_to_idx = {t: i for i, t in enumerate(unique_types)}
    n_types = len(unique_types)

    if n_types < 2:
        return np.nan, None

    # Build KDTree
    coords = np.column_stack([x, y])
    tree = cKDTree(coords)

    # Query k+1 neighbors (includes self)
    k_query = min(k + 1, len(coords))
    _, indices = tree.query(coords, k=k_query)

    # Build co-occurrence matrix
    cooc = np.zeros((n_types, n_types), dtype=np.float64)
    ct_idx = np.array([type_to_idx[c] for c in ct])

    for i in range(len(coords)):
        ci = ct_idx[i]
        for j_pos in range(1, k_query):  # skip self (position 0)
            neighbor = indices[i, j_pos]
            cj = ct_idx[neighbor]
            cooc[ci, cj] += 1

    # Normalize to probabilities
    total = cooc.sum()
    if total < 1:
        return np.nan, None

    probs = cooc / total
    flat = probs.flatten()
    flat = flat[flat > 0]

    H = -np.sum(flat * np.log2(flat))
    return H, cooc


