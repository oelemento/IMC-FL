# Main analysis
# ---------------------------------------------------------------------------
def run_h3c():
    print(f"\n{'='*70}")
    print(f"H3c: CD11c+HLA-DR+ DC-like Cells Define Tolerogenic Programs")
    print(f"{'='*70}")

    # --- Load data ---
    f = h5py.File(V8_PATH, 'r')
    f_utag = h5py.File(UTAG_PATH, 'r')

    sample_ids = load_array(f, 'sample_id')
    tma_arr = load_array(f, 'tma')
    cell_types = load_array(f, 'cell_type')
    tumor_mask = get_tumor_mask(sample_ids)
    cx = f['obs']['centroid_x'][:]
    cy = f['obs']['centroid_y'][:]

    n_total = len(sample_ids)
    n_tumor = np.sum(tumor_mask)
    print(f"S-panel: {n_total:,} total -> {n_tumor:,} tumor cells")

    # DC counts
    is_dc = cell_types == 'Dendritic cells'
    n_dc = np.sum(is_dc & tumor_mask)
    print(f"Dendritic cells in tumor: {n_dc:,} ({n_dc/n_tumor*100:.2f}%)")

    # Non-LQ mask
    non_lq = tumor_mask & ~np.isin(cell_types, list(LQ_TYPES))

    # Load marker data for IDO and VISTA
    marker_idx = get_marker_idx(f)
    ido_vals = load_marker(f, 'IDO', marker_idx)
    vista_vals = load_marker(f, 'VISTA', marker_idx)
    hla_dr_vals = load_marker(f, 'HLA_DR', marker_idx)
    cd11c_vals = load_marker(f, 'CD11c', marker_idx)

    print(f"\nMarker availability:")
    print(f"  IDO: {'loaded' if ido_vals is not None else 'NOT FOUND'}")
    print(f"  VISTA: {'loaded' if vista_vals is not None else 'NOT FOUND'}")
    print(f"  HLA_DR: {'loaded' if hla_dr_vals is not None else 'NOT FOUND'}")
    print(f"  CD11c: {'loaded' if cd11c_vals is not None else 'NOT FOUND'}")

    # Domains
    print(f"\nClassifying UTAG domains...")
    domains, foll_domains, inter_domains = classify_domains(f_utag, cell_types, PANEL)
    foll_mask = np.isin(domains, foll_domains) & tumor_mask
    inter_mask = np.isin(domains, inter_domains) & tumor_mask

    # =========================================================================
    # 1. Per-ROI DC fraction
    # =========================================================================
    print(f"\n--- 1. Per-ROI DC Fraction ---")
    unique_rois = sorted(set(sample_ids[tumor_mask]))
    roi_dc_frac = {}
    roi_dc_count = {}
    roi_tma = {}
    roi_typed_count = {}
    roi_entropy = {}

    for roi in unique_rois:
        rm = (sample_ids == roi) & tumor_mask
        typed = rm & ~np.isin(cell_types, list(LQ_TYPES))
        n_typed = np.sum(typed)

        # Min cells filter: Biomax=5000, others=8000
        tma_val = tma_arr[rm][0]
        min_c = 5000 if 'biomax' in tma_val.lower() else 8000
        if n_typed < min_c:
            continue

        n_dc_roi = np.sum(is_dc & rm)
        roi_dc_frac[roi] = n_dc_roi / n_typed
        roi_dc_count[roi] = n_dc_roi
        roi_tma[roi] = tma_val
        roi_typed_count[roi] = n_typed
        roi_entropy[roi] = shannon_entropy(cell_types[rm], LQ_TYPES)

    n_rois = len(roi_dc_frac)
    dc_fracs = np.array(list(roi_dc_frac.values()))
    print(f"ROIs passing filter: {n_rois}")
    print(f"DC fraction: mean={np.mean(dc_fracs)*100:.2f}%, "
          f"median={np.median(dc_fracs)*100:.2f}%, "
          f"range=[{np.min(dc_fracs)*100:.2f}%, {np.max(dc_fracs)*100:.2f}%]")

    # Per-TMA breakdown
    tmas = sorted(set(roi_tma.values()))
    tma_dc_groups = {}
    print(f"\n  {'TMA':>8} {'n_ROIs':>7} {'mean_DC%':>10} {'std':>8}")
    print("  " + "-" * 40)
    for t in tmas:
        vals = [roi_dc_frac[r] * 100 for r in roi_dc_frac if roi_tma[r] == t]
        tma_dc_groups[t] = vals
        print(f"  {t:>8} {len(vals):>7} {np.mean(vals):>10.2f} {np.std(vals):>8.2f}")

    # KW test across TMAs
    kw_groups = [tma_dc_groups[t] for t in tmas if len(tma_dc_groups[t]) >= 3]
    if len(kw_groups) >= 2:
        H_kw, p_kw = kruskal(*kw_groups)
        print(f"\n  Kruskal-Wallis (DC% across TMAs): H={H_kw:.2f}, p={p_kw:.4f}")
    else:
        p_kw = 1.0

    # =========================================================================
    # 2. DC domain enrichment: follicular vs interfollicular
    # =========================================================================
    print(f"\n--- 2. DC Domain Enrichment ---")
    filtered_rois = sorted(roi_dc_frac.keys())
    roi_dc_foll_frac = {}
    roi_dc_inter_frac = {}

    for roi in filtered_rois:
        rm = (sample_ids == roi) & tumor_mask
        # Follicular
        foll_typed = rm & foll_mask & ~np.isin(cell_types, list(LQ_TYPES))
        n_foll = np.sum(foll_typed)
        n_dc_foll = np.sum(is_dc & rm & foll_mask)
        # Interfollicular
        inter_typed = rm & inter_mask & ~np.isin(cell_types, list(LQ_TYPES))
        n_inter = np.sum(inter_typed)
        n_dc_inter = np.sum(is_dc & rm & inter_mask)

        if n_foll >= 100:
            roi_dc_foll_frac[roi] = n_dc_foll / n_foll
        if n_inter >= 100:
            roi_dc_inter_frac[roi] = n_dc_inter / n_inter

    common_domain_rois = sorted(set(roi_dc_foll_frac.keys()) & set(roi_dc_inter_frac.keys()))
    if len(common_domain_rois) >= 5:
        foll_vals = np.array([roi_dc_foll_frac[r] * 100 for r in common_domain_rois])
        inter_vals = np.array([roi_dc_inter_frac[r] * 100 for r in common_domain_rois])
        # Paired Wilcoxon (within-ROI)
        try:
            stat_w, p_wilcox = wilcoxon(inter_vals, foll_vals, alternative='two-sided')
        except Exception:
            stat_w, p_wilcox = 0, 1.0
        # Also unpaired MWU for robustness
        U_dom, p_dom = mannwhitneyu(inter_vals, foll_vals, alternative='two-sided')
        print(f"  n ROIs with both domains: {len(common_domain_rois)}")
        print(f"  Follicular DC%: mean={np.mean(foll_vals):.3f}%, "
              f"median={np.median(foll_vals):.3f}%")
        print(f"  Interfollicular DC%: mean={np.mean(inter_vals):.3f}%, "
              f"median={np.median(inter_vals):.3f}%")
        if np.mean(foll_vals) > 0:
            print(f"  Ratio (inter/foll): {np.mean(inter_vals)/np.mean(foll_vals):.2f}x")
        print(f"  Wilcoxon signed-rank: stat={stat_w:.0f}, p={p_wilcox:.2e}")
        print(f"  Mann-Whitney U: U={U_dom:.0f}, p={p_dom:.2e}")
    else:
        p_wilcox = 1.0
        p_dom = 1.0
        foll_vals = np.array([])
        inter_vals = np.array([])
        print("  Too few ROIs with both domain types")

    # =========================================================================
    # 3. DC proximity to T cells vs B cells
    # =========================================================================
    print(f"\n--- 3. DC Proximity to T cells vs B cells ---")

    dc_to_t_dists = []   # cell-level DC-to-nearest-T distances
    dc_to_b_dists = []   # cell-level DC-to-nearest-B distances
    dc_nnd_dists = []    # DC-to-DC nearest-neighbor distances (clustering)
    roi_dc_to_t_mean = {}
    roi_dc_to_b_mean = {}
    roi_dc_nnd_mean = {}

    n_skip_prox = 0
    for roi in filtered_rois:
        rm = (sample_ids == roi) & tumor_mask
        dc_mask = is_dc & rm
        n_dc_r = np.sum(dc_mask)
        if n_dc_r < MIN_DC:
            n_skip_prox += 1
            continue

        dc_xy = np.column_stack([cx[dc_mask], cy[dc_mask]])

        # T cells
        t_mask = np.isin(cell_types, T_CELL_TYPES) & rm
        n_t = np.sum(t_mask)
        if n_t >= 3:
            t_xy = np.column_stack([cx[t_mask], cy[t_mask]])
            tree_t = KDTree(t_xy)
            d_t, _ = tree_t.query(dc_xy, k=1)
            dc_to_t_dists.extend(d_t.tolist())
            roi_dc_to_t_mean[roi] = float(np.mean(d_t))

        # B cells
        b_mask = np.isin(cell_types, B_CELL_TYPES) & rm
        n_b = np.sum(b_mask)
        if n_b >= 3:
            b_xy = np.column_stack([cx[b_mask], cy[b_mask]])
            tree_b = KDTree(b_xy)
            d_b, _ = tree_b.query(dc_xy, k=1)
            dc_to_b_dists.extend(d_b.tolist())
            roi_dc_to_b_mean[roi] = float(np.mean(d_b))

        # DC-to-DC NND (spatial clustering)
        if n_dc_r >= 2:
            tree_dc = KDTree(dc_xy)
            d_dc, _ = tree_dc.query(dc_xy, k=2)
            d_dc_nn = d_dc[:, 1]  # skip self (k=0)
            dc_nnd_dists.extend(d_dc_nn.tolist())
            roi_dc_nnd_mean[roi] = float(np.mean(d_dc_nn))

    print(f"ROIs skipped (<{MIN_DC} DCs): {n_skip_prox}")
    dc_to_t_arr = np.array(dc_to_t_dists)
    dc_to_b_arr = np.array(dc_to_b_dists)
    dc_nnd_arr = np.array(dc_nnd_dists)
    print(f"DC-to-T cells: n={len(dc_to_t_arr):,}, "
          f"mean={np.mean(dc_to_t_arr):.1f} px, median={np.median(dc_to_t_arr):.1f} px")
    print(f"DC-to-B cells: n={len(dc_to_b_arr):,}, "
          f"mean={np.mean(dc_to_b_arr):.1f} px, median={np.median(dc_to_b_arr):.1f} px")
    print(f"DC-to-DC NND: n={len(dc_nnd_arr):,}, "
          f"mean={np.mean(dc_nnd_arr):.1f} px, median={np.median(dc_nnd_arr):.1f} px")

    # Paired per-ROI comparison: are DCs closer to T cells or B cells?
    common_prox_rois = sorted(set(roi_dc_to_t_mean.keys()) & set(roi_dc_to_b_mean.keys()))
    if len(common_prox_rois) >= 5:
        t_means = np.array([roi_dc_to_t_mean[r] for r in common_prox_rois])
        b_means = np.array([roi_dc_to_b_mean[r] for r in common_prox_rois])
        try:
            stat_prox, p_prox = wilcoxon(t_means, b_means, alternative='two-sided')
        except Exception:
            stat_prox, p_prox = 0, 1.0
        closer_to = 'T cells' if np.mean(t_means) < np.mean(b_means) else 'B cells'
        print(f"\n  Per-ROI paired comparison (DC-to-T vs DC-to-B):")
        print(f"    Mean DC-to-T: {np.mean(t_means):.1f} px")
        print(f"    Mean DC-to-B: {np.mean(b_means):.1f} px")
        print(f"    DCs are closer to: {closer_to}")
        print(f"    Wilcoxon: stat={stat_prox:.0f}, p={p_prox:.2e}")
    else:
        p_prox = 1.0
        t_means = np.array([])
        b_means = np.array([])
        closer_to = 'N/A'

    # =========================================================================
    # 4. DC fraction vs entropy correlation
    # =========================================================================
    print(f"\n--- 4. DC Fraction vs Entropy ---")
    common_ent_rois = sorted(set(roi_dc_frac.keys()) & set(roi_entropy.keys()))
    valid_ent_rois = [r for r in common_ent_rois if not np.isnan(roi_entropy[r])]
    if len(valid_ent_rois) >= 10:
        dc_frac_arr = np.array([roi_dc_frac[r] for r in valid_ent_rois])
        ent_arr = np.array([roi_entropy[r] for r in valid_ent_rois])
        rho_ent, p_ent = spearmanr(dc_frac_arr, ent_arr)
        print(f"  DC fraction vs entropy: rho={rho_ent:+.3f}, p={p_ent:.2e}")
    else:
        rho_ent, p_ent = 0, 1.0

    # =========================================================================
    # 5. IDO/VISTA expression on DCs vs other myeloid
    # =========================================================================
    print(f"\n--- 5. IDO/VISTA on DCs vs Other Myeloid ---")

    other_myeloid_mask = np.isin(cell_types, MYELOID_TYPES) & tumor_mask
    dc_tumor_mask = is_dc & tumor_mask

    for marker_name, marker_vals in [('IDO', ido_vals), ('VISTA', vista_vals)]:
        if marker_vals is None:
            print(f"  {marker_name}: not available")
            continue
        dc_expr = marker_vals[dc_tumor_mask]
        myeloid_expr = marker_vals[other_myeloid_mask]
        print(f"\n  {marker_name}:")
        print(f"    DCs (n={len(dc_expr):,}): mean={np.mean(dc_expr):.3f}, "
              f"median={np.median(dc_expr):.3f}")
        print(f"    Other myeloid (n={len(myeloid_expr):,}): mean={np.mean(myeloid_expr):.3f}, "
              f"median={np.median(myeloid_expr):.3f}")
        if len(dc_expr) >= 10 and len(myeloid_expr) >= 10:
            U_m, p_m = mannwhitneyu(dc_expr, myeloid_expr, alternative='two-sided')
            print(f"    MWU: U={U_m:.0f}, p={p_m:.2e}")
            if np.median(myeloid_expr) > 0:
                print(f"    Ratio (DC/myeloid median): "
                      f"{np.median(dc_expr)/np.median(myeloid_expr):.2f}x")

    # Also check HLA-DR and CD11c to confirm DC identity
    for marker_name, marker_vals in [('HLA_DR', hla_dr_vals), ('CD11c', cd11c_vals)]:
        if marker_vals is None:
            continue
        dc_expr = marker_vals[dc_tumor_mask]
        myeloid_expr = marker_vals[other_myeloid_mask]
        print(f"\n  {marker_name} (identity check):")
        print(f"    DCs: mean={np.mean(dc_expr):.3f}, "
              f"Other myeloid: mean={np.mean(myeloid_expr):.3f}")

    # =========================================================================
    # 6. Cell type drivers of DC fraction
    # =========================================================================
    print(f"\n--- 6. Cell Type Drivers of DC Fraction ---")
    roi_fracs = compute_roi_celltype_fractions(sample_ids, cell_types, tumor_mask, LQ_TYPES)
    drivers = correlate_celltype_with_metric(roi_fracs, roi_dc_frac)

    print(f"Drivers of DC fraction (Spearman rho):")
    for ct_name, rho, p in drivers[:15]:
        print(f"  {ct_name:40s}  rho={rho:+.3f}  p={p:.2e}")

    # =========================================================================
    # 7. Leave-one-TMA-out sensitivity
    # =========================================================================
    print(f"\n--- 7. Leave-One-TMA-Out Sensitivity ---")
    loo_results = []
    for excl in tmas:
        rois_keep = [r for r in filtered_rois if roi_tma[r] != excl]
        if len(rois_keep) < 5:
            loo_results.append((excl, 1.0, 1.0, 0.0))
            continue

        # Domain enrichment
        common_k = [r for r in rois_keep if r in roi_dc_foll_frac and r in roi_dc_inter_frac]
        if len(common_k) >= 5:
            fv = np.array([roi_dc_foll_frac[r] * 100 for r in common_k])
            iv = np.array([roi_dc_inter_frac[r] * 100 for r in common_k])
            try:
                _, p_loo_dom = wilcoxon(iv, fv, alternative='two-sided')
            except Exception:
                _, p_loo_dom = 0, 1.0
        else:
            p_loo_dom = 1.0

        # Proximity test
        common_prox_k = [r for r in rois_keep
                         if r in roi_dc_to_t_mean and r in roi_dc_to_b_mean]
        if len(common_prox_k) >= 5:
            tm = np.array([roi_dc_to_t_mean[r] for r in common_prox_k])
            bm = np.array([roi_dc_to_b_mean[r] for r in common_prox_k])
            try:
                _, p_loo_prox = wilcoxon(tm, bm, alternative='two-sided')
            except Exception:
                _, p_loo_prox = 0, 1.0
        else:
            p_loo_prox = 1.0

        # Entropy correlation
        ent_k = [r for r in rois_keep
                 if r in roi_dc_frac and r in roi_entropy and not np.isnan(roi_entropy[r])]
        if len(ent_k) >= 10:
            rho_k, _ = spearmanr(
                [roi_dc_frac[r] for r in ent_k],
                [roi_entropy[r] for r in ent_k])
        else:
            rho_k = 0.0

        print(f"  Excl {excl} (n={len(rois_keep)}): "
              f"domain p={p_loo_dom:.2e}, prox p={p_loo_prox:.2e}, "
              f"ent rho={rho_k:+.3f}")
        loo_results.append((excl, p_loo_dom, p_loo_prox, rho_k))

    # =========================================================================
    # Generate cartoon
    # =========================================================================
    cartoon_path = generate_cartoon()

    # =========================================================================
    # MAIN FIGURE: 2 x 3
    # =========================================================================
    print(f"\n--- Generating Main Figure ---")
    fig, axes = plt.subplots(2, 3, figsize=(18, 11))

    # (a) Concept cartoon
    add_cartoon(axes[0, 0], cartoon_path)
    label_panel(axes[0, 0], 'a')

    # (b) DC fraction vs entropy scatter
    ax = axes[0, 1]
    if len(valid_ent_rois) >= 10:
        for roi in valid_ent_rois:
            c = TMA_COLORS.get(roi_tma.get(roi, ''), '#999999')
            ax.scatter(roi_dc_frac[roi] * 100, roi_entropy[roi],
                       c=c, s=30, alpha=0.6, edgecolors='white', linewidths=0.3)
        # Regression line
        x_arr = np.array([roi_dc_frac[r] * 100 for r in valid_ent_rois])
        y_arr = np.array([roi_entropy[r] for r in valid_ent_rois])
        z = np.polyfit(x_arr, y_arr, 1)
        x_line = np.linspace(x_arr.min(), x_arr.max(), 100)
        ax.plot(x_line, np.polyval(z, x_line), 'k--', lw=1, alpha=0.5)
