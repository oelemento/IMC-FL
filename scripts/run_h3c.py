# ---------------------------------------------------------------------------
# Shannon entropy (clean, exclude LQ)
# ---------------------------------------------------------------------------
def shannon_entropy(ct_array, lq_types):
    clean = ct_array[~np.isin(ct_array, list(lq_types))]
    if len(clean) < 20:
        return np.nan
    counts = Counter(clean)
    total = sum(counts.values())
    props = np.array([c / total for c in counts.values()])
    props = props[props > 0]
    return float(-np.sum(props * np.log2(props)))


