# ---------------------------------------------------------------------------
# Boundary sharpness computation
# ---------------------------------------------------------------------------
def compute_boundary_sharpness(cx_roi, cy_roi, domains_roi, foll_domains,
                                b_cell_mask_roi, cd20_roi,
                                boundary_dist=BOUNDARY_DIST,
                                band_width=BAND_WIDTH,
                                gradient_inner=GRADIENT_INNER,
                                gradient_outer=GRADIENT_OUTER):
    """Compute boundary sharpness metrics for a single ROI.

    Returns dict with:
      - ratio_sharpness: B cell fraction inside foll / outside foll (within band)
      - gradient_sharpness: mean CD20 inner band - mean CD20 outer band
      - n_boundary_cells: number of cells near the boundary
      - b_frac_inner: B cell fraction inside follicular domains (within band)
      - b_frac_outer: B cell fraction outside follicular domains (within band)
    """
    is_foll = np.isin(domains_roi, foll_domains)
    n_foll = np.sum(is_foll)
    n_inter = np.sum(~is_foll)

    if n_foll < 20 or n_inter < 20:
        return None

    coords = np.column_stack([cx_roi, cy_roi])
    tree = KDTree(coords)

    # Find boundary cells: cells near the foll/inter interface
    # A cell is near the boundary if it is within boundary_dist of
    # at least one cell from the opposite domain
    foll_coords = coords[is_foll]
    inter_coords = coords[~is_foll]

    if len(foll_coords) == 0 or len(inter_coords) == 0:
        return None

    tree_foll = KDTree(foll_coords)
    tree_inter = KDTree(inter_coords)

    # For each follicular cell, distance to nearest interfollicular cell
    dist_foll_to_inter, _ = tree_inter.query(foll_coords, k=1)
    # For each interfollicular cell, distance to nearest follicular cell
    dist_inter_to_foll, _ = tree_foll.query(inter_coords, k=1)

    # Inner band: follicular cells within band_width of the boundary
    inner_band = is_foll.copy()
    inner_band[is_foll] &= (dist_foll_to_inter <= band_width)

    # Outer band: interfollicular cells within band_width of the boundary
    outer_band = (~is_foll).copy()
    outer_band[~is_foll] &= (dist_inter_to_foll <= band_width)

    n_inner = np.sum(inner_band)
    n_outer = np.sum(outer_band)

    if n_inner < 10 or n_outer < 10:
        return None

    # B cell fraction in inner vs outer band
    b_frac_inner = np.sum(b_cell_mask_roi[inner_band]) / n_inner
    b_frac_outer = np.sum(b_cell_mask_roi[outer_band]) / n_outer

    # Require minimum B cells in inner band (at least 5% to be a real follicle)
    if b_frac_inner < 0.05:
        return None

    # Ratio sharpness: B frac inside / B frac outside
    # Higher = sharper (B cells more confined to follicle)
    if b_frac_outer > 0.01:
        ratio_sharpness = b_frac_inner / b_frac_outer
    else:
        ratio_sharpness = b_frac_inner / 0.01  # avoid div by zero

    # Gradient sharpness: difference in mean CD20 intensity
    # Inner gradient band (0-gradient_inner px inside foll boundary)
    inner_gradient = is_foll.copy()
    inner_gradient[is_foll] &= (dist_foll_to_inter <= gradient_inner)
    # Outer gradient band (0-gradient_outer px outside foll boundary)
    outer_gradient = (~is_foll).copy()
    outer_gradient[~is_foll] &= (dist_inter_to_foll <= gradient_outer)

    n_ig = np.sum(inner_gradient)
    n_og = np.sum(outer_gradient)

    if n_ig < 5 or n_og < 5:
        gradient_sharpness = np.nan
    else:
        mean_cd20_inner = np.mean(cd20_roi[inner_gradient])
        mean_cd20_outer = np.mean(cd20_roi[outer_gradient])
        gradient_sharpness = mean_cd20_inner - mean_cd20_outer

    # Boundary cell mask for visualization
    boundary_mask = inner_band | outer_band
    n_boundary = np.sum(boundary_mask)

    return {
        'ratio_sharpness': ratio_sharpness,
        'gradient_sharpness': gradient_sharpness,
        'n_boundary_cells': n_boundary,
        'b_frac_inner': b_frac_inner,
        'b_frac_outer': b_frac_outer,
        'n_inner': n_inner,
        'n_outer': n_outer,
        'inner_band': inner_band,
        'outer_band': outer_band,
    }


# ---------------------------------------------------------------------------
# Follicularity and entropy per ROI
# ---------------------------------------------------------------------------
def compute_follicularity(sample_ids_roi, domains_roi, foll_domains):
    """Fraction of cells in follicular domains."""
    is_foll = np.isin(domains_roi, foll_domains)
    return np.sum(is_foll) / len(domains_roi) if len(domains_roi) > 0 else 0.0


def compute_clean_entropy(cell_types_roi):
    """Shannon entropy excluding LQ/Unassigned cells."""
    clean = cell_types_roi[~np.isin(cell_types_roi, list(LQ_TYPES))]
    if len(clean) < 20:
        return np.nan
    counts = Counter(clean)
    total = sum(counts.values())
    props = np.array([c / total for c in counts.values()])
    props = props[props > 0]
    return -np.sum(props * np.log2(props))


