def main():
    print(f"{'='*70}")
    print(f"H1a: Follicle Boundary Sharpness — S-panel")
    print(f"{'='*70}")

    # Generate concept cartoon
    cartoon_path = generate_cartoon()

    # Load data
    print("\nLoading S-panel v8 data...")
    f_v8 = h5py.File(V8_PATH, 'r')
    f_utag = h5py.File(UTAG_PATH, 'r')

    sample_ids = load_array(f_v8, 'sample_id')
    tma_arr = load_array(f_v8, 'tma')
    cell_types = load_array(f_v8, 'cell_type')
    cx = f_v8['obs']['centroid_x'][:]
    cy = f_v8['obs']['centroid_y'][:]
    marker_idx = get_marker_idx(f_v8)

    tumor_mask = get_tumor_mask(sample_ids)
    print(f"Total cells: {len(sample_ids):,}")
    print(f"Tumor cells: {np.sum(tumor_mask):,}")

    # Classify UTAG domains
    print("\nClassifying UTAG domains...")
    domains, foll_domains, inter_domains = classify_domains(f_utag, cell_types, PANEL)

    # Load CD20 marker
    cd20 = load_marker(f_v8, 'CD20', marker_idx)
    print(f"CD20 marker loaded (idx={marker_idx.get('CD20', 'N/A')})")

    # B cell mask (all B cell subtypes)
    b_cell_types = {'B cells', 'B cells (BCL2+)', 'B cells (PAX5+)'}
    b_cell_mask = np.isin(cell_types, list(b_cell_types))
    print(f"B cells: {np.sum(b_cell_mask & tumor_mask):,}")

    # FDC mask
    fdc_mask = cell_types == 'FDC'
    print(f"FDC cells: {np.sum(fdc_mask & tumor_mask):,}")

    # --- Compute boundary sharpness per ROI ---
    print("\nComputing boundary sharpness per ROI...")
    unique_rois = sorted(set(sample_ids[tumor_mask]))
    roi_data = []  # list of dicts
    skipped_min_cells = 0
    skipped_no_boundary = 0

    for i, roi in enumerate(unique_rois):
        if (i + 1) % 20 == 0:
            print(f"  Processing ROI {i+1}/{len(unique_rois)}...")

        roi_mask = (sample_ids == roi) & tumor_mask
        ct_roi = cell_types[roi_mask]

        # Min cells filter (non-LQ)
        n_typed = np.sum(~np.isin(ct_roi, list(LQ_TYPES)))
        if n_typed < MIN_CELLS:
            skipped_min_cells += 1
            continue

        cx_roi = cx[roi_mask]
        cy_roi = cy[roi_mask]
        domains_roi = domains[roi_mask]
        b_mask_roi = b_cell_mask[roi_mask]
        cd20_roi = cd20[roi_mask]
        fdc_mask_roi = fdc_mask[roi_mask]

        result = compute_boundary_sharpness(
            cx_roi, cy_roi, domains_roi, foll_domains,
            b_mask_roi, cd20_roi
        )

        if result is None:
            skipped_no_boundary += 1
            continue

        # Additional metrics
        tma_val = tma_arr[roi_mask][0]
        follicularity = compute_follicularity(sample_ids[roi_mask], domains_roi, foll_domains)
        entropy = compute_clean_entropy(ct_roi)
        fdc_frac = np.sum(fdc_mask_roi) / n_typed if n_typed > 0 else 0.0

        roi_data.append({
            'roi': roi,
            'tma': tma_val,
            'ratio_sharpness': result['ratio_sharpness'],
            'gradient_sharpness': result['gradient_sharpness'],
            'n_boundary_cells': result['n_boundary_cells'],
            'b_frac_inner': result['b_frac_inner'],
            'b_frac_outer': result['b_frac_outer'],
            'follicularity': follicularity,
            'entropy': entropy,
            'fdc_frac': fdc_frac,
            'n_typed': n_typed,
        })

    print(f"\nROIs processed: {len(roi_data)}")
    print(f"Skipped (min_cells): {skipped_min_cells}")
    print(f"Skipped (no boundary): {skipped_no_boundary}")

    if len(roi_data) < 10:
        print("ERROR: Too few ROIs with boundary data. Aborting.")
        f_v8.close()
        f_utag.close()
        return

    # Extract arrays
    rois = [d['roi'] for d in roi_data]
    tmas = [d['tma'] for d in roi_data]
    ratio_sharp = np.array([d['ratio_sharpness'] for d in roi_data])
    gradient_sharp = np.array([d['gradient_sharpness'] for d in roi_data])
    follicularity = np.array([d['follicularity'] for d in roi_data])
    entropy = np.array([d['entropy'] for d in roi_data])
    fdc_frac = np.array([d['fdc_frac'] for d in roi_data])
    b_frac_inner = np.array([d['b_frac_inner'] for d in roi_data])
    b_frac_outer = np.array([d['b_frac_outer'] for d in roi_data])

    # Use log(ratio) for better distribution
    log_ratio = np.log2(np.clip(ratio_sharp, 0.01, None))

    # --- Summary statistics ---
    print(f"\n{'='*50}")
    print("BOUNDARY SHARPNESS SUMMARY")
    print(f"{'='*50}")
    print(f"Ratio sharpness (B inner/outer): "
          f"mean={np.mean(ratio_sharp):.2f}, median={np.median(ratio_sharp):.2f}, "
          f"std={np.std(ratio_sharp):.2f}, range=[{np.min(ratio_sharp):.2f}, {np.max(ratio_sharp):.2f}]")
    print(f"Log2 ratio: mean={np.mean(log_ratio):.2f}, std={np.std(log_ratio):.2f}")
    valid_grad = ~np.isnan(gradient_sharp)
    print(f"Gradient sharpness (CD20 inner-outer): "
          f"mean={np.nanmean(gradient_sharp):.2f}, std={np.nanstd(gradient_sharp):.2f}, "
          f"n_valid={np.sum(valid_grad)}")
    print(f"B frac inner: mean={np.mean(b_frac_inner):.3f}")
    print(f"B frac outer: mean={np.mean(b_frac_outer):.3f}")

    # --- Correlations ---
    print(f"\n{'='*50}")
    print("CORRELATIONS (Spearman)")
    print(f"{'='*50}")

    # Ratio sharpness vs follicularity
    rho_foll, p_foll = spearmanr(log_ratio, follicularity)
    print(f"Log2(ratio) vs follicularity: rho={rho_foll:.3f}, p={p_foll:.2e}")

    # Ratio sharpness vs entropy
    valid_ent = ~np.isnan(entropy)
    rho_ent, p_ent = spearmanr(log_ratio[valid_ent], entropy[valid_ent])
    print(f"Log2(ratio) vs entropy: rho={rho_ent:.3f}, p={p_ent:.2e}")

    # Ratio sharpness vs FDC fraction
    rho_fdc, p_fdc = spearmanr(log_ratio, fdc_frac)
    print(f"Log2(ratio) vs FDC fraction: rho={rho_fdc:.3f}, p={p_fdc:.2e}")

    # Gradient vs follicularity
    if np.sum(valid_grad) > 10:
        rho_gf, p_gf = spearmanr(gradient_sharp[valid_grad], follicularity[valid_grad])
        print(f"Gradient vs follicularity: rho={rho_gf:.3f}, p={p_gf:.2e}")

    # --- Per-TMA breakdown ---
    print(f"\n{'='*50}")
    print("PER-TMA BREAKDOWN")
    print(f"{'='*50}")
    tma_unique = sorted(set(tmas))
    print(f"{'TMA':>8} {'n':>5} {'mean_ratio':>11} {'median_ratio':>13} {'mean_log2':>10}")
    tma_groups_lr = {}
    for t in tma_unique:
        idx_t = [i for i, tt in enumerate(tmas) if tt == t]
        vals_t = log_ratio[idx_t]
        tma_groups_lr[t] = vals_t
        print(f"{t:>8} {len(idx_t):>5} {np.mean(ratio_sharp[idx_t]):>11.2f} "
              f"{np.median(ratio_sharp[idx_t]):>13.2f} {np.mean(vals_t):>10.2f}")

    # --- Leave-one-TMA-out sensitivity ---
    print(f"\n{'='*50}")
    print("LEAVE-ONE-TMA-OUT SENSITIVITY")
    print(f"{'='*50}")
    loo_results = []
    for exclude in tma_unique:
        idx_keep = [i for i, tt in enumerate(tmas) if tt != exclude]
        lr_keep = log_ratio[idx_keep]
        foll_keep = follicularity[idx_keep]
        ent_keep = entropy[idx_keep]
        fdc_keep = fdc_frac[idx_keep]

        rho_f, p_f = spearmanr(lr_keep, foll_keep)
        valid_e = ~np.isnan(ent_keep)
        rho_e, p_e = spearmanr(lr_keep[valid_e], ent_keep[valid_e]) if np.sum(valid_e) > 5 else (np.nan, np.nan)
        rho_d, p_d = spearmanr(lr_keep, fdc_keep)

        loo_results.append({
            'excluded': exclude,
            'n': len(idx_keep),
            'rho_foll': rho_f, 'p_foll': p_f,
            'rho_ent': rho_e, 'p_ent': p_e,
            'rho_fdc': rho_d, 'p_fdc': p_d,
        })
        print(f"Exclude {exclude} (n={len(idx_keep)}): "
              f"foll rho={rho_f:.3f} p={p_f:.2e}, "
              f"entropy rho={rho_e:.3f} p={p_e:.2e}, "
              f"FDC rho={rho_d:.3f} p={p_d:.2e}")

    # --- Driver analysis ---
    print(f"\n{'='*50}")
    print("DRIVER ANALYSIS (cell type correlations with sharpness)")
    print(f"{'='*50}")
    roi_metric = {d['roi']: lr for d, lr in zip(roi_data, log_ratio)}
    roi_fracs = compute_roi_celltype_fractions(sample_ids, cell_types, tumor_mask, LQ_TYPES)
    drivers = correlate_celltype_with_metric(roi_fracs, roi_metric)
    for ct, rho, p in drivers[:15]:
        print(f"  {ct:40s}  rho={rho:+.3f}  p={p:.2e}")

    # -----------------------------------------------------------------------
    # MAIN FIGURE (2x3)
    # -----------------------------------------------------------------------
    print("\nGenerating main figure...")
    fig, axes = plt.subplots(2, 3, figsize=(18, 11))

    # (a) Concept cartoon
    add_cartoon(axes[0, 0], cartoon_path)
    label_panel(axes[0, 0], 'a')

    # (b) Distribution of boundary sharpness
    ax = axes[0, 1]
    for t in tma_unique:
        idx_t = [i for i, tt in enumerate(tmas) if tt == t]
        ax.hist(log_ratio[idx_t], bins=15, alpha=0.5, color=TMA_COLORS.get(t, 'gray'),
                label=f'{t} (n={len(idx_t)})', edgecolor='white')
    ax.axvline(np.mean(log_ratio), color='black', linestyle='--', lw=1.5,
               label=f'Mean = {np.mean(log_ratio):.2f}')
    ax.axvline(np.median(log_ratio), color='gray', linestyle=':', lw=1.5,
               label=f'Median = {np.median(log_ratio):.2f}')
    ax.set_xlabel('Boundary Sharpness (log2 B-cell ratio)', fontsize=10)
    ax.set_ylabel('Number of ROIs', fontsize=10)
    ax.set_title(f'Distribution of Boundary Sharpness\n(n={len(roi_data)} tumor ROIs)', fontsize=11)
    ax.legend(fontsize=7)
    label_panel(ax, 'b')

    # (c) Scatter: sharpness vs follicularity
    ax = axes[0, 2]
    for t in tma_unique:
        idx_t = [i for i, tt in enumerate(tmas) if tt == t]
        ax.scatter(follicularity[idx_t], log_ratio[idx_t], c=TMA_COLORS.get(t, 'gray'),
                   alpha=0.6, s=25, edgecolors='white', linewidths=0.3, label=t)
    # Add regression line
    z = np.polyfit(follicularity, log_ratio, 1)
    x_line = np.linspace(follicularity.min(), follicularity.max(), 100)
    ax.plot(x_line, np.polyval(z, x_line), 'k--', alpha=0.5, lw=1.5)
    ax.set_xlabel('Follicularity (fraction in follicular domains)', fontsize=10)
    ax.set_ylabel('Boundary Sharpness (log2 ratio)', fontsize=10)
    ax.set_title(f'Sharpness vs Follicularity\n(Spearman rho={rho_foll:.3f}, p={p_foll:.2e})', fontsize=11)
    ax.legend(fontsize=7, loc='best')
    label_panel(ax, 'c')

    # (d) Representative spatial plots: sharp vs diffuse boundary
    ax = axes[1, 0]
    # Find sharp and diffuse ROIs (85th and 15th percentile)
    p15_val = np.percentile(log_ratio, 15)
    p85_val = np.percentile(log_ratio, 85)
    idx_lo = np.argmin(np.abs(log_ratio - p15_val))
    idx_hi = np.argmin(np.abs(log_ratio - p85_val))
    roi_lo = roi_data[idx_lo]['roi']
    roi_hi = roi_data[idx_hi]['roi']

    ax.axis('off')
    ax_lo = ax.inset_axes([0.0, 0.0, 0.47, 0.88])
    ax_hi = ax.inset_axes([0.53, 0.0, 0.47, 0.88])

    for sub_ax, roi_name, val, label in [
        (ax_lo, roi_lo, log_ratio[idx_lo], 'Diffuse'),
        (ax_hi, roi_hi, log_ratio[idx_hi], 'Sharp')
    ]:
        m = (sample_ids == roi_name) & tumor_mask
        x_all, y_all = cx[m], cy[m]
        dom = domains[m]
        ct_all = cell_types[m]
        b_m = b_cell_mask[m]
        is_foll = np.isin(dom, foll_domains)
        is_inter = np.isin(dom, inter_domains)

        # Domain background tint
        if np.any(is_foll):
            sub_ax.scatter(x_all[is_foll], y_all[is_foll], c='#FFDDDD', s=5,
                          alpha=0.3, edgecolors='none', rasterized=True, zorder=0)
        if np.any(is_inter):
            sub_ax.scatter(x_all[is_inter], y_all[is_inter], c='#DDDDFF', s=5,
                          alpha=0.3, edgecolors='none', rasterized=True, zorder=0)

        # All cells as gray
        sub_ax.scatter(x_all, y_all, c='#D3D3D3', s=0.3, alpha=0.4,
                      edgecolors='none', rasterized=True, zorder=1)

        # Compute boundary cells for this ROI
        coords_roi = np.column_stack([x_all, y_all])
        foll_coords = coords_roi[is_foll]
        inter_coords = coords_roi[~is_foll]

        if len(foll_coords) > 0 and len(inter_coords) > 0:
            tree_f = KDTree(foll_coords)
            tree_i = KDTree(inter_coords)
            dist_f2i, _ = tree_i.query(foll_coords, k=1)
            dist_i2f, _ = tree_f.query(inter_coords, k=1)

            boundary_foll = np.zeros(len(x_all), dtype=bool)
            boundary_inter = np.zeros(len(x_all), dtype=bool)
